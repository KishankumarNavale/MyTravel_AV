package com.av.view;



public class Users {
	

	  private static String userName = new String();
	  private static String password = new String();
	  private static String role = new String();
	  private static String users_pk = new String();
	public static String getUserName() {
		return userName;
	}
	public static void setUserName(String userName) {
		Users.userName = userName;
	}
	public static String getPassword() {
		return password;
	}
	public static void setPassword(String password) {
		Users.password = password;
	}
	public static String getRole() {
		return role;
	}
	public static void setRole(String role) {
		Users.role = role;
	}
	public static String getUsers_pk() {
		return users_pk;
	}
	public static void setUsers_pk(String users_pk) {
		Users.users_pk = users_pk;
	}




	
	  
	

}
